CREATE TABLE "ROLE" (

	ROLEID INTEGER NOT NULL, 

	TITLE VARCHAR(1000), 
PRIMARY KEY (ROLEID)
);



CREATE TABLE EMPLOYEE (

	EMPLOYEEID INTEGER NOT NULL, 

	"NAME" VARCHAR(1000), 
"ROLE" INTEGER, 

	"ROLENAME" VARCHAR(1000),

	PRIMARY KEY (EMPLOYEEID),
CONSTRAINT FK_ROLEID FOREIGN KEY (ROLE) REFERENCES ROLE(ROLEID)
);




